package aplicacion.modelo.dominio;

/**
 *
 * @author Alumno
 */
public class Libros {
    private String titulo;
    private String autor;
    private int precio;
    private int ISBN;

    public Libros() {
    }

    public Libros(String titulo, String autor, int precio, int ISBN) {
        this.titulo = titulo;
        this.autor = autor;
        this.precio = precio;
        this.ISBN = ISBN;
    }

 

    /**
     * @return the titulo
     */
    public String getTitulo() {
        return titulo;
    }

    /**
     * @param titulo the titulo to set
     */
    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    /**
     * @return the autor
     */
    public String getAutor() {
        return autor;
    }

    /**
     * @param autor the autor to set
     */
    public void setAutor(String autor) {
        this.autor = autor;
    }

    /**
     * @return the precio
     */
    public int getPrecio() {
        return precio;
    }

    /**
     * @param precio the precio to set
     */
    public void setPrecio(int precio) {
        this.precio = precio;
    }

    /**
     * @return the ISBN
     */
    public int getISBN() {
        return ISBN;
    }

    /**
     * @param ISBN the ISBN to set
     */
    public void setISBN(int ISBN) {
        this.ISBN = ISBN;
    }

    
    
}
